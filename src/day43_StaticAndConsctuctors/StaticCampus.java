package day43_StaticAndConsctuctors;

public class StaticCampus {
	
	
	// When static method is added to a class it means those belong to the class 
	// directly and can be used/called directly with class name without creating an
	// object
	
	
	
	public static String city;
	
	public static void aboutCampus() {
		
		System.out.println("Campus is located in "+city);
	}

}
