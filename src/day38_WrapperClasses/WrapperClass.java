package day38_WrapperClasses;

public class WrapperClass {
	
	public static void main(String[] args) {
		
		// Wrapper Classes convert primitives to an object to be used in data structures
		// like Array Lists
		// Display min and max values in wrapper classes
		
		System.out.println("Max Char " + Character.MIN_VALUE); // ! Type Character wrapper class
		System.out.println("Max Char " + Character.MAX_VALUE);
		
		System.out.println("Min Int " + Integer.MIN_VALUE); // Type Integer in full
		System.out.println("Max Int " + Integer.MAX_VALUE);
		
		System.out.println("Min Byte " + Byte.MIN_VALUE);
		System.out.println("Max Byte " + Byte.MAX_VALUE);
		
		System.out.println("Max Short " + Short.MAX_VALUE);
		System.out.println("Max Short " + Short.MAX_VALUE);
		
		System.out.println("Max Float " + Float.MAX_VALUE);
		System.out.println("Max Float " + Float.MAX_VALUE);
		
		System.out.println("Max Double " + Double.MAX_VALUE);
		System.out.println("Max Double " + Double.MAX_VALUE);
		
		System.out.println("Max Long " + Long.MAX_VALUE);
		System.out.println("Max Long " + Long.MAX_VALUE);
		
		
		
	}

}
